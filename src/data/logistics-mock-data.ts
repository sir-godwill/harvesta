export const getAlertSeverityColor = (severity: string) => {
  const colors: Record<string, string> = {
    low: 'bg-blue-500/10 text-blue-500',
    warning: 'bg-warning/10 text-warning',
    critical: 'bg-destructive/10 text-destructive',
  };
  return colors[severity] || 'bg-muted';
};

export const getAlertStatusColor = (status: string) => {
  const colors: Record<string, string> = {
    active: 'bg-destructive/10 text-destructive',
    acknowledged: 'bg-warning/10 text-warning',
    investigating: 'bg-accent/10 text-accent',
    resolved: 'bg-success/10 text-success',
  };
  return colors[status] || 'bg-muted';
};
